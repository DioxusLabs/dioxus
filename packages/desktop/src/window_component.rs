/// Properties for the [`Window()`] component.
///
/// Use the `config` prop to set the initial native window configuration, the
/// `onclose` prop to handle a user-initiated close, and children to choose what
/// the window renders. Configuration is applied when the window is opened.
#[derive(Clone)]
pub struct WindowProps {
    config: Rc<RefCell<Option<Config>>>,
    onclose: Option<EventHandler<()>>,
    children: Element,
}

impl Properties for WindowProps {
    type ComponentBuilder<RenderFn, Marker> = WindowComponentBuilder<RenderFn, Marker, ()>;

    fn component_builder<RenderFn, Marker>(
        render_fn: RenderFn,
    ) -> Self::ComponentBuilder<RenderFn, Marker> {
        WindowComponentBuilder {
            render_fn,
            builder: WindowPropsBuilder {
                config: None,
                onclose: None,
                children: (),
            },
            _marker: PhantomData,
        }
    }

    fn memoize(&mut self, new: &Self) -> bool {
        self.onclose = new.onclose;
        self.children = new.children.clone();
        false
    }
}

impl<RenderFn, Marker> dioxus_core::ComponentBuilderRender<RenderFn, Marker> for WindowProps
where
    RenderFn: ComponentFunction<WindowProps, Marker>,
    Marker: 'static,
{
    fn into_vcomponent(self, render_fn: RenderFn) -> VComponent {
        <Self as Properties>::into_vcomponent(self, render_fn)
    }
}

/// A [`WindowProps`] builder that remembers the [`Window()`] component function it came from,
/// mirroring the builder generated by `#[derive(Props)]`. The `rsx!` macro starts from the
/// component function, so the field setters and `build` live on this wrapper rather than on
/// [`WindowPropsBuilder`].
#[doc(hidden)]
#[must_use]
#[allow(non_camel_case_types, non_snake_case)]
pub struct WindowComponentBuilder<RenderFn, ComponentMarker, Children> {
    render_fn: RenderFn,
    builder: WindowPropsBuilder<Children>,
    _marker: PhantomData<ComponentMarker>,
}

#[allow(missing_docs)]
impl<RenderFn, ComponentMarker, Children>
    WindowComponentBuilder<RenderFn, ComponentMarker, Children>
{
    pub fn config(self, config: Config) -> Self {
        WindowComponentBuilder {
            render_fn: self.render_fn,
            builder: self.builder.config(config),
            _marker: self._marker,
        }
    }

    pub fn onclose<Marker>(
        self,
        onclose: impl SuperInto<Option<EventHandler<()>>, Marker>,
    ) -> Self {
        WindowComponentBuilder {
            render_fn: self.render_fn,
            builder: self.builder.onclose(onclose),
            _marker: self._marker,
        }
    }
}

#[allow(missing_docs)]
impl<RenderFn, ComponentMarker> WindowComponentBuilder<RenderFn, ComponentMarker, ()> {
    pub fn children(
        self,
        children: Element,
    ) -> WindowComponentBuilder<RenderFn, ComponentMarker, Element> {
        WindowComponentBuilder {
            render_fn: self.render_fn,
            builder: self.builder.children(children),
            _marker: self._marker,
        }
    }

    pub fn build(self) -> ComponentBuilderOutput<RenderFn, WindowProps, ComponentMarker> {
        ComponentBuilderOutput::new(self.render_fn, self.builder.build())
    }
}

#[allow(missing_docs)]
impl<RenderFn, ComponentMarker> WindowComponentBuilder<RenderFn, ComponentMarker, Element> {
    pub fn build(self) -> ComponentBuilderOutput<RenderFn, WindowProps, ComponentMarker> {
        ComponentBuilderOutput::new(self.render_fn, self.builder.build())
    }
}

